    private void resetForm() {
        // Clear Text Fields
        TxtNamaBarang.setText("");
        TxtHarga.setText("");
        TxtStok.setText("");

        // Reset ComboBox and RadioButtons
        CmbKategori.setSelectedIndex(0);
        groupButtonStatus.clearSelection();

        // Reset Spinner
        SpinnerStok.setValue(0);
    }

    private void BtnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTambahActionPerformed
        // TODO add your handling code here:
        // Validate Inputs
        if (TxtNamaBarang.getText().isEmpty() || TxtHarga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Barang dan Harga harus diisi!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validate Stock Input
        int stok = 0; // Default value
        boolean isSpinnerUsed = false; // To track which input is used

        if (!TxtStok.getText().isEmpty()) {
            // User provided input via TextField
            try {
                stok = Integer.parseInt(TxtStok.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Jumlah Stok di TextField harus berupa angka!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } else {
            // Default to Spinner if TextField is empty
            stok = (Integer) SpinnerStok.getValue();
            isSpinnerUsed = true;
        }

        if (stok <= 0) {
            JOptionPane.showMessageDialog(this, "Jumlah Stok harus lebih dari 0!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Retrieve Other Inputs
        String namaBarang = TxtNamaBarang.getText();
        String kategori = CmbKategori.getSelectedItem().toString();
        String status = RadioBtnBaru.isSelected() ? "Baru" : "Bekas";
        double harga;

        try {
            harga = Double.parseDouble(TxtHarga.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga harus berupa angka!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Add to Table
        int no = tableModel.getRowCount() + 1; // Auto-incrementing number
// Auto-incrementing number
        tableModel.addRow(new Object[]{no, namaBarang, kategori, status, stok, harga});
        // Success Message
        JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan" + "!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

        // Reset Form
        resetForm();
    }//GEN-LAST:event_BtnTambahActionPerformed

