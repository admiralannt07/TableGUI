    private void BtnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnHapusActionPerformed
        // TODO add your handling code here:
        int selectedRow = OutputTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih baris yang ingin dihapus!", "Kesalahan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Confirm Deletion
        int confirm = JOptionPane.showConfirmDialog(this,
                "Apakah Anda yakin ingin menghapus data ini?",
                "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            // Remove Selected Row from Table
            tableModel.removeRow(selectedRow);

            // Success Message
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

            // Reset Form
            resetForm();
        }
    }//GEN-LAST:event_BtnHapusActionPerformed

    private void BtnTotalStokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnTotalStokActionPerformed
        // TODO add your handling code here:
        if (tableModel.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Tabel kosong. Tidak ada stok yang dapat dihitung!", "Kesalahan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Calculate Total Stock
        int totalStock = 0;
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            totalStock += (int) tableModel.getValueAt(i, 4); // Column 4 = Stock Amount
        }

        // Display Total Stock
        JOptionPane.showMessageDialog(this, "Total Stok: " + totalStock, "Total Stok", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_BtnTotalStokActionPerformed

