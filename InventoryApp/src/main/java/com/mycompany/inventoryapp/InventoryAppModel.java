    private void BtnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEditActionPerformed
        // TODO add your handling code here:
        // Ensure a row is selected
        int selectedRow = OutputTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Silakan pilih baris yang ingin diubah!", "Kesalahan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validate Inputs
        if (TxtNamaBarang.getText().isEmpty() || TxtHarga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Barang dan Harga harus diisi!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int stok = 0; // Stock Amount
        if (!TxtStok.getText().isEmpty()) {
            // Use TextField if not empty
            try {
                stok = Integer.parseInt(TxtStok.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Jumlah Stok di TextField harus berupa angka!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
                return;
            }
        } else {
            // Default to Spinner
            stok = (Integer) SpinnerStok.getValue();
        }

        if (stok <= 0) {
            JOptionPane.showMessageDialog(this, "Jumlah Stok harus lebih dari 0!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String namaBarang = TxtNamaBarang.getText();
        String kategori = CmbKategori.getSelectedItem().toString();
        String status = RadioBtnBaru.isSelected() ? "Baru" : "Bekas";
        double harga;

        try {
            harga = Double.parseDouble(TxtHarga.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga harus berupa angka!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Update Selected Row in the Table
        tableModel.setValueAt(namaBarang, selectedRow, 1); // Update Item Name
        tableModel.setValueAt(kategori, selectedRow, 2);   // Update Category
        tableModel.setValueAt(status, selectedRow, 3);     // Update Status
        tableModel.setValueAt(stok, selectedRow, 4);       // Update Stock Amount
        tableModel.setValueAt(harga, selectedRow, 5);      // Update Item Cost

        // Success Message
        JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

        // Reset Form
        resetForm();

    }//GEN-LAST:event_BtnEditActionPerformed

